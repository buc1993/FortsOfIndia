    <img src="Daulatabad-Fort.jpg" style="width:240px;height:150px;">
    <p>Though the sand of time has left its imprints behind the Daulatabad Fort, it is still can be called one of the best documents containing the history of the city.</p>
    <p>The fort overlooks the green views of the vegetation around and is certainly a photographer’s paradise. With its vintage look, it never ceases to amaze the visitors.</p>
</div>

<div class="fort">
